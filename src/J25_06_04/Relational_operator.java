package J25_06_04;

public class Relational_operator {
    public static void main(String[] args){
        int a = 10;
        int b = 7;

        System.out.println("a == b : " + (a == b));
        System.out.println("a != b : " + (a != b));

        System.out.println("a > b : " + (a > b));
        System.out.println("a < b : " + (a < b));
        System.out.println("a >= 10 : " + (a >= 10));
        System.out.println("a <= 5 : " + (a <= 5));


    }
}
